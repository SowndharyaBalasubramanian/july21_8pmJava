package com.operations;

public class Relational {

	public static void main(String[] args) {
		int c = 8; int d = 8;
		System.out.println(c == d );
		System.out.println(c != d);
		System.out.println(c < d);
		System.out.println(c > d);
		System.out.println(c <= d);
		System.out.println(c >= d);

	}

}
